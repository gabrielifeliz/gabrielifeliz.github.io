/* Function to sort country list by country name in alphabetical order
   Credit: https://www.w3schools.com/howto/howto_js_sort_list.asp
   I made changes for the function to work here
*/
function sortList() {
  let i, switching, b, shouldSwitch;
  switching = true;
  /* Make a loop that will continue until
  no switching has been done: */
  while (switching) {
    // start by saying: no switching is done:
    switching = false;
    b = document.querySelector("ol").getElementsByTagName("li");
    // Loop through all list-items:
    for (i = 0; i < b.length - 1; i++) {
      // start by saying there should be no switching:
      shouldSwitch = false;
      /* check if the next item should
      switch place with the current item: */
      if (
        b[i].innerHTML.split(" ")[1].toLowerCase() >
        b[i + 1].innerHTML.split(" ")[1].toLowerCase()
      ) {
        /* if next item is alphabetically
        lower than current item, mark as a switch
        and break the loop: */
        shouldSwitch = true;
        break;
      }
    }
    if (shouldSwitch) {
      /* If a switch has been marked, make the switch
      and mark the switch as done: */
      b[i].parentNode.insertBefore(b[i + 1], b[i]);
      switching = true;
    }
  }
}

